// Declaram variabilele si constanta PI
var r = parseInt(prompt('Introduceti raza: '))
var h = parseInt(prompt('Introduceti inaltimea: '))
var v
const pi = 3.141592

// Calculam volumul
v = r * r * pi * h

// Afisam rezultatul
console.log('Volumul cilindrului este: ' + v)
document.write('Volumul cilindrului este ' + v)